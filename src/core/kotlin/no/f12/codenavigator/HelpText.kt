package no.f12.codenavigator

object HelpText {
    fun generate(tool: BuildTool = BuildTool.GRADLE): String = buildString {
        val t = { goal: String -> tool.taskName(goal) }
        val p = { name: String, value: String -> tool.param(name, value) }
        val pf = { name: String -> tool.paramFlag(name) }
        fun u(goal: String, vararg params: String) = tool.usage(goal, *params)

        appendLine("=== code-navigator plugin ===")
        appendLine()
        appendLine("All tasks support ${p("format", "json")} for machine-readable JSON output.")
        appendLine()
        appendLine("Available tasks:")
        appendLine()
        appendLine("  ${t("help")}")
        appendLine("    Shows this help text.")
        appendLine("    Usage: ${u("help")}")
        appendLine()
        appendLine("  ${t("list-classes")}")
        appendLine("    Lists all classes in the project and their source files.")
        appendLine("    Scans compiled bytecode from the main source set.")
        appendLine("    Usage: ${u("list-classes")}")
        appendLine()
        appendLine("  ${t("find-class")}")
        appendLine("    Searches for classes matching a regex pattern.")
        appendLine("    Matches against both class name and source file path (case-insensitive).")
        appendLine("    Parameters:")
        appendLine("      ${p("pattern", "<regex>")}  (required) Regex to match against class names and source paths")
        appendLine("    Usage: ${u("find-class", p("pattern", "Service"))}")
        appendLine("    Usage: ${u("find-class", p("pattern", "\".*Reset.*\""))}")
        appendLine("    Usage: ${u("find-class", p("pattern", "\"domain\\\\..*\""))}")
        appendLine()
        appendLine("  ${t("find-symbol")}")
        appendLine("    Searches for symbols (methods, fields) matching a regex pattern.")
        appendLine("    Matches against package, class name, symbol name, and source file (case-insensitive).")
        appendLine("    Filters out constructors, synthetic methods, property accessors, and data class generated methods.")
        appendLine("    Output columns: Package | Class | Symbol | Kind | Source File")
        appendLine("    Parameters:")
        appendLine("      ${p("pattern", "<regex>")}  (required) Regex to match against symbol information")
        appendLine("    Usage: ${u("find-symbol", p("pattern", "resetPassword"))}")
        appendLine("    Usage: ${u("find-symbol", p("pattern", "\".*Service.*\""))}")
        appendLine("    Usage: ${u("find-symbol", p("pattern", "\"find.*\""))}")
        appendLine()
        appendLine("  ${t("find-callers")}")
        appendLine("    Shows who calls a given method, displayed as an indented tree.")
        appendLine("    Scans compiled bytecode to build a call graph, then walks callers transitively.")
        appendLine("    Parameters:")
        appendLine("      ${p("method", "<regex>")}      (required) Regex to match against Class.method names")
        appendLine("      ${p("maxdepth", "<N>")}        (required) Maximum depth of caller tree")
        appendLine("      ${p("projectonly", "true")}     (optional) Filter to project classes only, hiding JDK/stdlib noise")
        appendLine("    Usage: ${u("find-callers", p("method", "resetPassword"), p("maxdepth", "3"))}")
        appendLine("    Usage: ${u("find-callers", p("method", "\".*Service\\\\.find.*\""), p("maxdepth", "5"))}")
        appendLine("    Usage: ${u("find-callers", p("method", "\"Repo\\\\.save\""), p("maxdepth", "5"), p("projectonly", "true"))}")
        appendLine()
        appendLine("  ${t("find-callees")}")
        appendLine("    Shows what a method calls, displayed as an indented tree.")
        appendLine("    Scans compiled bytecode to build a call graph, then walks callees transitively.")
        appendLine("    Parameters:")
        appendLine("      ${p("method", "<regex>")}      (required) Regex to match against Class.method names")
        appendLine("      ${p("maxdepth", "<N>")}        (required) Maximum depth of callee tree")
        appendLine("      ${p("projectonly", "true")}     (optional) Filter to project classes only, hiding JDK/stdlib noise")
        appendLine("    Usage: ${u("find-callees", p("method", "resetPassword"), p("maxdepth", "3"))}")
        appendLine("    Usage: ${u("find-callees", p("method", "\".*Controller\\\\.handle.*\""), p("maxdepth", "5"))}")
        appendLine("    Usage: ${u("find-callees", p("method", "\"Service\\\\.create\""), p("maxdepth", "5"), p("projectonly", "true"))}")
        appendLine()
        appendLine("  ${t("class-detail")}")
        appendLine("    Shows class signature: fields, methods, superclass, and implemented interfaces.")
        appendLine("    Filters out constructors, synthetic methods, property accessors, and data class boilerplate.")
        appendLine("    Parameters:")
        appendLine("      ${p("pattern", "<regex>")}  (required) Regex to match against class names")
        appendLine("    Usage: ${u("class-detail", p("pattern", "ResetPasswordService"))}")
        appendLine("    Usage: ${u("class-detail", p("pattern", "\".*Client\""))}")
        appendLine("    Usage: ${u("class-detail", p("pattern", "\"domain\\\\.DomainError\""))}")
        appendLine()
        appendLine("  ${t("find-interfaces")}")
        appendLine("    Finds classes that implement a given interface.")
        appendLine("    Scans compiled bytecode to build an interface-to-implementors map.")
        appendLine("    Parameters:")
        appendLine("      ${p("pattern", "<regex>")}     (required) Regex to match against interface names")
        appendLine("      ${p("includetest", "true")}    (optional) Include test source set to find test fakes/stubs")
        appendLine("    Usage: ${u("find-interfaces", p("pattern", "Repository"))}")
        appendLine("    Usage: ${u("find-interfaces", p("pattern", "\".*Client\""))}")
        appendLine("    Usage: ${u("find-interfaces", p("pattern", "\"services\\\\.interfaces\\\\..*\""))}")
        appendLine("    Usage: ${u("find-interfaces", p("pattern", "Repository"), p("includetest", "true"))}")
        appendLine()
        appendLine("  ${t("package-deps")}")
        appendLine("    Shows package-level dependencies — which packages call into which other packages.")
        appendLine("    Aggregates the call graph by package, excluding self-dependencies.")
        appendLine("    Without ${p("package", "<regex>")}, shows all packages. With ${p("package", "<regex>")}, filters to matching packages.")
        appendLine("    With ${p("reverse", "true")}, shows reverse dependencies (who depends on each package).")
        appendLine("    Parameters:")
        appendLine("      ${p("package", "<regex>")}    (optional) Regex to match against package names")
        appendLine("      ${p("reverse", "true")}       (optional) Show reverse dependencies (incoming instead of outgoing)")
        appendLine("      ${p("projectonly", "true")}   (optional) Filter to project classes only, hiding JDK/stdlib noise")
        appendLine("    Usage: ${u("package-deps")}")
        appendLine("    Usage: ${u("package-deps", p("package", "services"))}")
        appendLine("    Usage: ${u("package-deps", p("package", "\"ktor\\\\.routes\""))}")
        appendLine("    Usage: ${u("package-deps", p("reverse", "true"))}")
        appendLine("    Usage: ${u("package-deps", p("package", "domain"), p("reverse", "true"))}")
        appendLine("    Usage: ${u("package-deps", p("projectonly", "true"))}")
        appendLine()
         appendLine("  ${t("find-usages")}")
        appendLine("    Finds project references to external types or methods.")
        appendLine("    Scans compiled bytecode for method calls, field accesses, and type references")
        appendLine("    matching the specified ownerClass/method or type. Owner-aware — distinguishes")
        appendLine("    same-named methods on different types. Finds Kotlin property accessors by")
        appendLine("    their bytecode name (e.g., getMonthNumber for .monthNumber).")
        appendLine("    Parameters:")
        appendLine("      ${p("ownerClass", "<class>")}   (required*) FQN of type — matches method call and field owners")
        appendLine("      ${p("method", "<name>")}        (optional) Specific method/field name on the owner")
        appendLine("      ${p("type", "<class>")}         (required*) Find ALL references to a class: calls, fields, casts, signatures")
        appendLine("      ${p("outside-package", "<pkg>")}(optional) Exclude callers inside this package (show only external usages)")
        appendLine("      * Provide either 'ownerClass' or 'type'. Use 'type' for comprehensive search.")
        appendLine("    Usage: ${u("find-usages", p("ownerClass", "kotlinx.datetime.LocalDate"), p("method", "getMonthNumber"))}")
        appendLine("    Usage: ${u("find-usages", p("ownerClass", "kotlinx.datetime.Clock"))}")
        appendLine("    Usage: ${u("find-usages", p("type", "kotlinx.datetime.Instant"))}")
        appendLine("    Usage: ${u("find-usages", p("type", "kotlinx.datetime.Instant"), p("outside-package", "com.example.datetime"))}")
        appendLine()
        appendLine("  ${t("dsm")}")
        appendLine("    Shows a Dependency Structure Matrix (DSM) — a compact grid view of inter-package")
        appendLine("    dependencies. Row depends on column; cells show reference counts. Highlights cyclic")
        appendLine("    dependencies with class-level detail.")
        appendLine("    Parameters:")
        appendLine("      ${p("root-package", "<pkg>")}   (optional) Only include packages under this prefix")
        appendLine("      ${p("dsm-depth", "<N>")}        (optional) Package depth to group by (default: 2)")
        appendLine("      ${p("dsm-html", "<path>")}      (optional) Write interactive HTML matrix to file")
        appendLine("      ${p("cycles", "true")}           (optional) Show only cyclic dependencies with class-level edges")
        appendLine("      ${p("cycle", "<pkgA>,<pkgB>")}  (optional) Show only the cycle between two specific packages")
        appendLine("    Usage: ${u("dsm")}")
        appendLine("    Usage: ${u("dsm", p("root-package", "com.example"), p("dsm-depth", "3"))}")
        appendLine("    Usage: ${u("dsm", p("dsm-html", "build/dsm.html"))}")
        appendLine("    Usage: ${u("dsm", p("cycles", "true"))}")
        appendLine("    Usage: ${u("dsm", p("cycle", "api,service"))}")
        appendLine()
        appendLine("  ${t("rank")}")
        appendLine("    Ranks types by structural importance using PageRank on the call graph.")
        appendLine("    Types that are called by many important types score higher. Useful for")
        appendLine("    identifying core abstractions, god classes, and critical dependencies.")
        appendLine("    Parameters:")
        appendLine("      ${p("top", "<N>")}              (optional) Maximum results to return (default: 50)")
        appendLine("      ${p("projectonly", "true")}     (optional) Filter to project classes only (default: true)")
        appendLine("    Usage: ${u("rank")}")
        appendLine("    Usage: ${u("rank", p("top", "20"))}")
        appendLine("    Usage: ${u("rank", p("projectonly", "false"))}")
        appendLine()
        appendLine("  ${t("dead")}")
        appendLine("    Finds potential dead code — classes and methods never referenced by other project code.")
        appendLine("    Reports classes with no incoming type-level references and methods with no cross-class callers.")
        appendLine("    Kotlin noise (data class boilerplate, companion objects, coroutine inner classes,")
        appendLine("    bridge methods, static initializers) is automatically filtered out.")
        appendLine("    Results are 'potential' dead code — reflection-based usage cannot be detected from bytecode.")
        appendLine("    Parameters:")
        appendLine("      ${p("filter", "<regex>")}       (optional) Only show results matching this regex")
        appendLine("      ${p("exclude", "<regex>")}      (optional) Exclude results matching this regex (e.g. entry points, tests)")
        appendLine("      ${p("classes-only", "true")}    (optional) Show only unreferenced classes, skip dead methods")
        appendLine("    Usage: ${u("dead")}")
        appendLine("    Usage: ${u("dead", p("filter", "\"service\""))}")
        appendLine("    Usage: ${u("dead", p("exclude", "\"Main|Test|Application\""))}")
        appendLine("    Usage: ${u("dead", p("classes-only", "true"))}")
        appendLine()
        appendLine("  ${t("complexity")}")
        appendLine("    Shows fan-in/fan-out complexity for classes matching a regex pattern.")
        appendLine("    Fan-out: how many calls go from this class to other classes (outgoing coupling).")
        appendLine("    Fan-in: how many calls come into this class from other classes (incoming coupling).")
        appendLine("    Parameters:")
        appendLine("      ${p("classname", "<pattern>")}      (required) Class name regex to analyze")
        appendLine("      ${p("projectonly", "true")}     (optional) Filter to project classes only (default: true)")
        appendLine("      ${p("detail", "true")}          (optional) Show individual call details")
        appendLine("    Usage: ${u("complexity", p("classname", "Service"))}")
        appendLine("    Usage: ${u("complexity", p("classname", "\".*Controller\""), p("projectonly", "false"))}")
        appendLine("    Usage: ${u("complexity", p("classname", "\"domain\\\\..*\""), p("detail", "true"))}")
        appendLine()
        appendLine("  ${t("agent-help")}")
        appendLine("    Shows detailed instructions for AI coding agents on how to use code-navigator effectively.")
        appendLine("    Includes recommended workflows, task selection guidance, and tips for optimal results.")
        appendLine("    Usage: ${u("agent-help")}")
        appendLine()
        appendLine("  ${t("config-help")}")
        appendLine("    Lists all available configuration parameters with defaults")
        appendLine("    and which tasks they apply to.")
        appendLine("    Usage: ${u("config-help")}")
        appendLine()
        appendLine("--- Git History Analysis Tasks ---")
        appendLine()
        appendLine("These tasks analyze git history and do NOT require compilation.")
        appendLine("All share the parameter: ${p("after", "YYYY-MM-DD")} (default: 1 year ago)")
        appendLine("Git rename tracking is enabled by default (-M flag). Use ${pf("no-follow")} to disable it.")
        appendLine()
        appendLine("  ${t("hotspots")}")
        appendLine("    Ranks files by change frequency. The most-changed files are where development")
        appendLine("    effort concentrates — priority refactoring targets if they also have structural problems.")
        appendLine("    Parameters:")
        appendLine("      ${p("after", "YYYY-MM-DD")}     (optional) Only consider commits after this date (default: 1 year ago)")
        appendLine("      ${p("min-revs", "N")}           (optional) Minimum revisions to include (default: 1)")
        appendLine("      ${p("top", "N")}                (optional) Maximum results to return (default: 50)")
        appendLine("    Usage: ${u("hotspots")}")
        appendLine("    Usage: ${u("hotspots", p("after", "2024-01-01"), p("min-revs", "5"), p("top", "20"))}")
        appendLine()
        appendLine("  ${t("coupling")}")
        appendLine("    Finds files that change together in the same commits — implicit dependencies invisible")
        appendLine("    in call graphs. Complements structural ${t("package-deps")} with behavioral coupling data.")
        appendLine("    Parameters:")
        appendLine("      ${p("after", "YYYY-MM-DD")}          (optional) Only consider commits after this date (default: 1 year ago)")
        appendLine("      ${p("min-shared-revs", "N")}         (optional) Minimum shared commits to report (default: 5)")
        appendLine("      ${p("min-coupling", "N")}            (optional) Minimum coupling degree % (default: 30)")
        appendLine("      ${p("max-changeset-size", "N")}      (optional) Skip commits touching more files (default: 30)")
        appendLine("    Usage: ${u("coupling")}")
        appendLine("    Usage: ${u("coupling", p("min-shared-revs", "10"), p("min-coupling", "50"))}")
        appendLine()
        appendLine("  ${t("code-age")}")
        appendLine("    Measures time since last modification per file. Stable old code is good;")
        appendLine("    frequently-changing old code with structural problems is a hotspot.")
        appendLine("    Parameters:")
        appendLine("      ${p("after", "YYYY-MM-DD")}     (optional) Only consider commits after this date (default: 1 year ago)")
        appendLine("      ${p("top", "N")}                (optional) Maximum results to return (default: 50)")
        appendLine("    Usage: ${u("code-age")}")
        appendLine("    Usage: ${u("code-age", p("after", "2023-01-01"), p("top", "20"))}")
        appendLine()
        appendLine("  ${t("authors")}")
        appendLine("    Number of distinct contributors per file. More developers on a module means")
        appendLine("    larger communication challenges. High author counts correlate with defects.")
        appendLine("    Parameters:")
        appendLine("      ${p("after", "YYYY-MM-DD")}     (optional) Only consider commits after this date (default: 1 year ago)")
        appendLine("      ${p("min-revs", "N")}           (optional) Minimum revisions to include (default: 1)")
        appendLine("      ${p("top", "N")}                (optional) Maximum results to return (default: 50)")
        appendLine("    Usage: ${u("authors")}")
        appendLine("    Usage: ${u("authors", p("min-revs", "3"), p("top", "20"))}")
        appendLine()
        appendLine("  ${t("churn")}")
        appendLine("    Measures lines added/deleted per file. Pre-release churn is a good predictor")
        appendLine("    of post-release defects.")
        appendLine("    Parameters:")
        appendLine("      ${p("after", "YYYY-MM-DD")}     (optional) Only consider commits after this date (default: 1 year ago)")
        appendLine("      ${p("top", "N")}                (optional) Maximum results to return (default: 50)")
        appendLine("    Usage: ${u("churn")}")
        appendLine("    Usage: ${u("churn", p("after", "2024-06-01"), p("top", "30"))}")
    }
}
